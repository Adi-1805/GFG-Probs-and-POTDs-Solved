class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int count = 0;
        int temp = N*10;
        while(temp){
            temp /= 10;
            if(temp%10 == 0) continue;
            else count += (N%(temp%10) == 0);
        }
        return count;
    }
};

